class Switchcase
{
	public static void main(String[] args)
	{
		char optn='A';
		System.out.println(optn);
		switch(optn)
		{
			case 'A':System.out.println("A");
					break;
			case 'B':System.out.println("B");
					break;		
			case 'C':System.out.println("C");
					break;		
		}
	}
}