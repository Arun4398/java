class BitwiseOperator
{
	public static void main(String[] args) 
	{
	int a=20;
	int b=10;
	int c;

	System.out.println("Bitwise AND(&)");
	c=a&b;
	System.out.println(c);

	System.out.println("Bitwise OR(|)");
	c=a|b;
	System.out.println(c);

	System.out.println("Bitwise XOP(^)");
	c=a^b;
	System.out.println(c);
	}
}